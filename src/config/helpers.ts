
export const getShoeMaterialName = (
  name: string
) => {
  switch (name) {
    case "side":
      return "Side";
    case "back_flipper":
      return "Back Flipper";
    case "front_down":
      return "Front Down";
    case "slashes":
      return "Slashes";
    case "mini_flaps":
      return "Mini Flaps";
    case "side_flaps":
      return "Side Flaps";
    case "back_flip":
      return "Back Flip";
    case "logo":
      return "Logo";
    case "upper_side":
      return "Upper Side";
    case "upper_soft":
      return "Upper Soft";
    case "softy":
      return "Softy";
    case "big_front":
      return "Big Front";
    case "upper_bottom_bottom":
      return "Upper Bottom Bottom";
    case "bottooom":
      return "Bottooom";
    case "bottom_logo":
      return "Bottom Logo";
    case "middle_sides":
      return "Middle Sides";
    case "front_side":
      return "Front Side";
  }
};